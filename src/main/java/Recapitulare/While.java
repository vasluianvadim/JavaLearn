package Recapitulare;

public class While {
    public static void main(String[] args) {
        int n = 0;
        while (n < 12) {
            System.out.println(n);
            n++;
        }
        int i =1;
        do{
            System.out.println(i);
            i++;
        }while (i<6);
    }
}
